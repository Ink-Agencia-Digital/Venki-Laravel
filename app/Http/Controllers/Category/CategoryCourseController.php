     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request, Category $category)
    {
        if ($request->has('user_id')) {
            $courses = $category->courses()->with(['users' => function ($query) use ($request) {
                return $query->where('users.id', $request->user_id);
            }]);

            return $courses->get();
        }

        return $this->collectionResponse(CourseResource::collection($this->getModel(new Course, [], $category->courses())));
    }

